static QString entryid( ULONG sz, LPENTRYID eid )
{
    QString s;
    LPBYTE foo;

    s += QString("cb: %1 lpb: ").arg(sz);
    foo = (LPBYTE)eid;
    for ( int i = 0; i < sz; i++ ) {
        s += QString().sprintf("%02x", (*foo));
        foo++;
    }
    
    return s;
}

QString guid( void *data )
{
    QString guid;
    LPBYTE foo = (LPBYTE)data;
    for ( int i = 0; i < sizeof(GUID); i++ ) {
        guid += QString().sprintf("%02x", (*foo));
        foo++;
    }
    return guid;
}

